package com.syntax.class03;

public class ShorthandOperators {

	public static void main(String[] args) {
		
		// +,-,*,/,%
		
		int num=100;
		num=num+100;
		
		System.out.println(num);
		
		num+=100;
		System.out.println(num);
		
		num-=20;
		System.out.println(num);
		
		num/=10; //shorter way to put num=num/10
		System.out.println(num);
		
		num%=3;
		System.out.println(num); //1 remainer after division
		
		
		int boo=188;
		boo-=5;
		
		System.out.println(boo);
		
		boo%=18;
		System.out.println(boo);
		
		
		//3
		int cakePiece=11;
		cakePiece /=4;
		System.out.println(cakePiece);
		
		int cake=25;
		cake%=7;
		System.out.println(cake);
		
		
		
	
		
		
		
			
		
			
	}

}


